/**
 * src/domain/planning.js
 *
 * Planning domain logic, ported from scripts/planejar.mjs (CLI) for the browser.
 * No Node dependencies (fs, path, readline, etc.).
 *
 * Main exports:
 *   buildCourseGraph(ppcJson)               -> { nodes, prereqs, coreqs, suggestedSemester }
 *   buildOfferIndex(offerJson, turno)           -> Map<codigo, { codigo, periodo, nome, carga_horaria, turmas }>
 *   inferNextSemester(rows, anoInicio)           -> { courseTerm, ano, offerTerm }
 *   generateSemester(params)                        -> { newRows, courseTerm, ano, offerTerm }
 *   enrichRowsWithOffer(rows, s1, s2, turno)    -> PlanningRow[]  (fills turma horarios from offer)
 *   upsertSemester(rows, sc, newRows)            -> PlanningRow[]
 *   groupUnique(rows)                            -> PlanningRow[]
 */


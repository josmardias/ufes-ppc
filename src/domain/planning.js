/**
 * src/domain/planning.js
 *
 * Planning domain logic, ported from scripts/planejar.mjs for the browser.
 * No Node dependencies (fs, path, readline, etc.).
 *
 * Main exports:
 *   buildGraphFromPpcJson(ppcJson)               -> { nodes, prereqs, coreqs, suggestedSemester }
 *   buildOfferIndex(ofertaJson, turno)           -> Map<codigo, { codigo, periodo, nome, carga_horaria, turmas }>
 *   inferNextSemester(rows, anoInicio)           -> { semestreCurso, ano, semestreOferta }
 *   gerarSemestre(params)                        -> { newRows, semestreCurso, ano, semestreOferta }
 *   enrichRowsWithOferta(rows, s1, s2, turno)    -> PlanningRow[]  (fills turma horarios from offer)
 *   upsertSemester(rows, sc, newRows)            -> PlanningRow[]
 *   groupUnique(rows)                            -> PlanningRow[]
 */

// ---------------------------------------------------------------------------
// Internal helpers
// ---------------------------------------------------------------------------

function normalizeCourseId(id) {
  const trimmed = String(id ?? "").trim();
  const m = trimmed.match(/^\d+\.([A-Z]{2,}\d{3,})$/);
  if (m) return m[1];
  return trimmed;
}

function hhmmToMinutes(hhmm) {
  const m = String(hhmm ?? "")
    .trim()
    .match(/^(\d{1,2}):(\d{2})$/);
  if (!m) return null;
  const hh = Number(m[1]);
  const mm = Number(m[2]);
  if (!Number.isFinite(hh) || !Number.isFinite(mm)) return null;
  if (hh < 0 || hh > 23 || mm < 0 || mm > 59) return null;
  return hh * 60 + mm;
}

function turmaMatchesTurno(turma, turno) {
  if (turno === "dia") return true;

  const slots = Array.isArray(turma?.horarios) ? turma.horarios : [];
  if (slots.length === 0) return true;

  const CUTOFF = 13 * 60; // 13:00

  if (turno === "manha") {
    return slots.some((h) => {
      const min = hhmmToMinutes(String(h?.inicio ?? "").trim());
      return min !== null && min < CUTOFF;
    });
  }
  if (turno === "tarde") {
    return slots.some((h) => {
      const min = hhmmToMinutes(String(h?.inicio ?? "").trim());
      return min !== null && min >= CUTOFF;
    });
  }
  return true;
}

function normalizeTurmasFromOferta(disciplina, turno = "dia") {
  const turmas = Array.isArray(disciplina?.turmas) ? disciplina.turmas : [];
  const filtered =
    turno === "dia"
      ? turmas
      : turmas.filter((t) => turmaMatchesTurno(t, turno));

  return filtered.map((t) => ({
    codigo: String(t?.turma ?? t?.codigo ?? "").trim(),
    horarios: Array.isArray(t?.horarios)
      ? t.horarios
          .map((h) => ({
            dia: String(h?.dia ?? "").trim(),
            inicio: String(h?.inicio ?? "").trim(),
            fim: String(h?.fim ?? "").trim(),
          }))
          .filter((h) => h.dia && h.inicio && h.fim)
      : [],
    docente: String(t?.docente ?? "").trim(),
  }));
}

